#include<math.h>
using namespace std;
const double PI=3.14;
class cylinder{
private:
	double radius;
	double height;
public:
	cylinder(double &radius,double &height)
	{
		PI*pow(radius,2)*height;
		return 0;
	}
	vol()
	{
		cout<<"the cylinder volume is:"cout<<cylinder();
	}
};
